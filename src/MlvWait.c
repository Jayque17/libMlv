/******************************************************************************/
/* wait_seconds : Suspend l'execution pendant le nombre specifie de secondes. */
/*                                                                            */
/* wait_seconds (                                                             */
/*    int           seconds)            Nombre entier de secondes a attendre  */
/******************************************************************************/

/******************************************************************************/
/* Including files                                                            */
/******************************************************************************/
#include  <stdlib.h>
#include  <sys/types.h>
#include  <sys/time.h>

/******************************************************************************/
/* main                                                                       */
/******************************************************************************/
void wait_seconds (
   int              seconds)                  /* Execution delay (in seconds) */
{
/******************************************************************************/
/* Local variables                                                            */
/******************************************************************************/
   struct timeval tv;

/******************************************************************************/
/* Check parameters                                                           */
/******************************************************************************/
   if (seconds > 0)
   {
      tv.tv_sec = seconds;
      tv.tv_usec = 0;
      select(0,NULL,NULL,NULL,&tv);
   }
}

