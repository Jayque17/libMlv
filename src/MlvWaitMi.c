/******************************************************************************/
/* wait_micros : Suspend l'execution pendant le nombre specifie de micro-     */
/*               secondes (=1.e-6 seconde).                                   */
/*                                                                            */
/* wait_micros (                                                              */
/*    int           micros)             Nombre entier de micro-secondes       */
/******************************************************************************/

/******************************************************************************/
/* Including files                                                            */
/******************************************************************************/
#include  <stdlib.h>
#include  <sys/types.h>
#include  <sys/time.h>

/******************************************************************************/
/* main                                                                       */
/******************************************************************************/
void wait_micros (
   int               micros)            /* Execution delay (in micro-seconds) */
{
/******************************************************************************/
/* Local variables                                                            */
/******************************************************************************/
   struct timeval tv;
     
/******************************************************************************/
/* Check parameters                                                           */
/******************************************************************************/
   if (micros > 0)
   {
      tv.tv_sec = micros / 1000000;
      tv.tv_usec = micros % 1000000;
      select(0,NULL,NULL,NULL,&tv);
   }
}
